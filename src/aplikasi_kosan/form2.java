    private void btnprosesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnprosesActionPerformed
        // TODO add your handling code here:
        int lamainap, diskon=0, hargaperbulan=0, totalbayar=0, totalbersih=0;
        String type = "";

        lamainap = Integer.parseInt(txtlama.getText());
        type=cmtypekamar.getSelectedItem().toString();

        if(type.compareTo("Ekonomi")==0) {
            hargaperbulan=350000;
        } else if(type.compareTo("Deluxe")==0) {
            hargaperbulan=450000;
        } else if(type.compareTo("VIP")==0 ) {
            hargaperbulan=550000;
        } else if(type.compareTo("President")==0 ) {
            hargaperbulan=850000;
        }

        totalbayar=hargaperbulan*lamainap;

        if(lamainap>5) {
            diskon=(int)(totalbayar*0.05);
        } else if (lamainap>10) {
            diskon=(int)(totalbayar*0.10);
        } else if (lamainap>15) {
            diskon=(int)(totalbayar*0.15);
        } else {
            diskon=0;
        }

        totalbersih=totalbayar-diskon;
        txttotalBersih.setText(String.valueOf(totalbersih));
        txtharga.setText(String.valueOf(hargaperbulan));
        txtbayar.setText(String.valueOf(totalbayar));
        txtdiskon.setText(String.valueOf(diskon));
    }//GEN-LAST:event_btnprosesActionPerformed

