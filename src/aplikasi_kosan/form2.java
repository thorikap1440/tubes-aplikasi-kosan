    public void table() {
        DefaultTableModel tbl = new DefaultTableModel();
        tbl.addColumn("ID");
        tbl.addColumn("Nama");
        tbl.addColumn("Type");
        tbl.addColumn("Harga");
        tbl.addColumn("Lama");
        tbl.addColumn("Bayar");
        tbl.addColumn("Diskon");
        tbl.addColumn("Total Bersih");
        
        try{
            Statement st = (Statement) koneksi.getConnection().createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM tbl_pelanggan");
            
            while(rs.next()){
                tbl.addRow(new Object[] {
                   rs.getString("id_pelanggan"),
                   rs.getString("Nama_Pelanggan"),
                   rs.getString("Type_Kamar"),
                   rs.getString("Harga_Perbulan"),
                   rs.getString("Lama_Kost"),
                   rs.getString("Total_bayar"),
                   rs.getString("Diskon"),
                   rs.getString("Total_Bersih"),
                });
                tblPelanggan.setModel(tbl);
            }
            JOptionPane.showMessageDialog(null, "Koneksi Database Berhasil");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Koneksi Database Gagal" + e.getMessage());
        }
   
    }
       
    

